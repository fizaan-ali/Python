a = ("apple", "banana", "cherry") # This is a tuple represented by parentheses
print(type(a))
print(a)

b = () # empty tuple
print(type(b))
print(b)

c = (1,) # This is how to create a single element tuple - with a comma
print(type(c))
print(c)

d = (1) # This is not a tuple, it's just an integer within parentheses
print(type(d))
print(d)